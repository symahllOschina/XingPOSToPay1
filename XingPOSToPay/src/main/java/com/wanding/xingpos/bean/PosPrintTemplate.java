package com.wanding.xingpos.bean;


/**
 * pos打印膜版
 */
public class PosPrintTemplate {

    /**
     * 商户号
     */
    private String mchntId;

    /**
     * 商户名称
     */
    private String mchntName;

    /**
     * 终端ID
     */
    private String termId;

    /**
     * 终端名称
     */
    private String termName;

    /**
     * 业务类型
     */
    private String businessType;

    /**
     * 交易类型
     */
    private String tradeType;

    /**
     * 订单号
     */
    private String orderId;

    /**
     * 交易时间，格式yyyy-MM-dd HH:mm:ss
     */
    private String transTime;

    /**
     * 交易金额，单位元
     */
    private String transAmount;

    /**
     * 备注
     */
    private String remark;

    /**
     * roomType适用房型
     */
    private String roomType;


    /**
     * timelimite 适用场次
     */
    private String timeLimite;



    public PosPrintTemplate() {
    }

    public String getMchntId() {
        return mchntId;
    }

    public void setMchntId(String mchntId) {
        this.mchntId = mchntId;
    }

    public String getMchntName() {
        return mchntName;
    }

    public void setMchntName(String mchntName) {
        this.mchntName = mchntName;
    }

    public String getTermId() {
        return termId;
    }

    public void setTermId(String termId) {
        this.termId = termId;
    }

    public String getTermName() {
        return termName;
    }

    public void setTermName(String termName) {
        this.termName = termName;
    }

    public String getBusinessType() {
        return businessType;
    }

    public void setBusinessType(String businessType) {
        this.businessType = businessType;
    }

    public String getTradeType() {
        return tradeType;
    }

    public void setTradeType(String tradeType) {
        this.tradeType = tradeType;
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getTransTime() {
        return transTime;
    }

    public void setTransTime(String transTime) {
        this.transTime = transTime;
    }

    public String getTransAmount() {
        return transAmount;
    }

    public void setTransAmount(String transAmount) {
        this.transAmount = transAmount;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getRoomType() {
        return roomType;
    }

    public void setRoomType(String roomType) {
        this.roomType = roomType;
    }

    public String getTimeLimite() {
        return timeLimite;
    }

    public void setTimeLimite(String timeLimite) {
        this.timeLimite = timeLimite;
    }
}
